        Elektroterapia
    </h2>
    <p>
        Prądy to jeden z elementów rehabilitacji to przede wszystkim przynoszące ulgę w dolegliwościach bólowych oraz w stanach zapalnych. Zabiegi elektroterapii pobudzają również głębiej położone warstwy tkanek i mięśni, poprawiają ich ukrwienie i odżywienie, prowadząc tym samym do ich szybszej regeneracji. Tkanka stymulowana prądem staje się lepiej ukrwiona dlatego przyspiesza to wchłanianie obrzęków i wysięków. W stanach pourazowych elektroterapia pozwala przywrócić osłabionym mięśniom siłę, w przypadku przykurczów związanych z przebiegiem różnych chorób.
    </p>

    <h3 class="page__secondHeader">
        Rodzaje elektroterapii
    </h3>
    <p>
        Wyróżniamy kilka rodzajów elektroterapii:<br/>
        <span class="bold">Jonosfera</span> – zabieg polegający na aplikowaniu prądu stałego w chore miejsce. Tego rodzaju elektroterapię stosuje się w leczeniu nerwobóli, neuralgii, chorób reumatycznych ora w stanach zapalnych. <br/>
        <span class="bold">Galwanizacja</span> – to podanie pradu stałego o małej częstotliwości. Wpływa on na tkanki nerwowe oraz mięśnie. Stosuję się ją w dyskopatii, nerwobólach, stanach zapalnych nerwów, do leczenia przeciążeń i napięć mięśniowych oraz w przypadku choroby zwyrodnieni stawów. <br/>
        <span class="bold">Elektrostymulacja</span> – to zabieg polegający na podaniu prądu impulsowego w celu wywołania skurczu mięśni. Stosuję się ją w leczeniu niedowładów oraz porażeń i innych dolegliwości ukladu nerwowego. <br/>
        <span class="bold">Prądy TENS</span> – to prądy impulsowe o małej częstotliwości. Stymulują nerwy i stosowane są przy bólach kręgosłupa, bólach stawowo-mięśniowych oraz reumatycznych. Zalecane są osobom odczuwającym bóle pooperacyjne a także pacjentom z owrzodzeniami skóry, dyskopatia, neuropatią oraz bólami towarzyszącymi osteoporozie. Mają korzystne działanie w przypadku cellulitu oraz przyspieszaj zrost złamanych kości. <br/>
        <span class="bold">Prądy diadynamiczne</span> – obniżają napięcie mięśniowe, zwiększają przepływ krwi, przyspieszają wchłanianie się obrzęków i krwiaków. Stosowane przy bólach kręgosłupa, chorobach zwyrodnieniowych stawów, schorzeniach reumatycznych, przy dyskopatiach, nerwobólach i dystrofii mięśniowej, po urazach narządu ruchu zwłaszcza z obrzękiem. <br/>
        <span class="bold">Prądy interferencyjne</span> – mają działanie przeciwbólowe, poprawiają krążenie, regenerują tkanki. <br/>
        <span class="bold">Prądy Kotza</span> – to zabieg polegający na wywołaniu skurczów głębiej położonych włókien mięśniowych. Stosowany w leczeniu skoliozy, przy osłabieniu mięśni, po urazach np. złamaniach. Prądy Traberta – zabieg polegający na podawaniu jednokierunkowych, impulsowych prądów. Stosowane przy chorobie zwyrodnieniowej stawów, w przypadku bólów kręgosłupa i bólów mięśniowych, w leczeniu nerwobóli i zaburzeniach krążenia obwodowego, także przy niedokrwieniu kończyn, także w stanach pourazowych narządy ruchu. <br/>
        <span class="bold">Przezskórna stymulacja nerwów</span> – ma na celu działanie przeciwbólowe.
