    <h3 class="page__secondHeader">
        Korekcja wad postawy
    </h3>
    <p>
        Przy pojawieniu się wad postawy dochodzi do osłabienia jednych mięśni, natomiast inne mogą być nadmiernie napięte. Podczas korekcji postawy fizjoterapeuta stara się uzyskać jak największą równowagę napięcia mięśni. Wymaga to systematycznych prawidłowo wykonywanych ćwiczeń, a czasem terapii manualnej. Zmiany czynnościowe na poziomie napięcia mięśni są w pełni odwracalne, dlatego nie warto bagatelizować pierwszych oznak braku symetrii w wyglądzie tkanek miękkich ciała.
    <p>
    <p>
